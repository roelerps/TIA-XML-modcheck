package plcProgramModel;

/**
 * The Enum VariableDatatype.
 */
public enum VariableDatatype {

	/** The real variable datatype. */
	REAL,
	/** The integer variable datatype. */
	INT,
	/** The boolean variable datatype. */
	BOOL,
	/** The time variable datatype. */
	TIME,
	/** The timer variable datatype. */
	TIMER,
	/** The udt variable datatype. */
	UDT,
	/** The double integer variable datatype. */
	DINT,
	/** The local and unknown variable datatype. */
	LOCALUNKNOWN,
	/** The void variable datatype. */
	VOID
}
